function findUserWithMostSkills(object)
{
    const objectEntries = Object.entries(object);
    let userWithMostSkills = objectEntries[0][0];
    let userWithMostSkillsLength = objectEntries[0][1].skills.length;

    objectEntries.map((entry) =>
    {
        if (entry[1].skills.length > userWithMostSkillsLength)
        {
            userWithMostSkills = entry[0];
            userWithMostSkillsLength = entry[1];
        }
    });

    return (userWithMostSkills);
}

